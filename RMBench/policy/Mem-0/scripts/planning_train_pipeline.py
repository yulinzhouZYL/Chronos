#!/usr/bin/env python3
"""
Planning Module pipeline (steps: copy, train, merge). Step 'prepare' is run by run_planning_pipeline.sh.
This script is invoked by run_planning_pipeline.sh with all config passed as CLI args. Do not edit this file;
edit the variables in run_planning_pipeline.sh instead.
"""

from __future__ import annotations

import argparse
import json
import os
import shutil
import subprocess
import sys
from pathlib import Path

# Defaults (overridden by CLI from run_planning_pipeline.sh)
USER_CONFIG = {
    "lerobot_dataset_path": "",
    "episode_start_id": 0,
    "episode_end_id": 50,
    "llamafactory_root": "",
    "base_output_dir": "",
    "max_samples": 1000,
    "num_train_epochs": 25,
    "per_device_train_batch_size": 16,
    "learning_rate": 1.0e-4,
    "export_dir": "",
    "export_size": 5,
    "export_device": "cpu",
    "conda_env_mem0": "mem0",
    "conda_env_llamafactory": "llama_factory",
    "report_to": "wandb",
}

BASE_MODEL_DIR_NAME = "Qwen3-VL-8B-Instruct"
TEMPLATE = "qwen3_vl_nothink"
TRAIN_YAML_NAME = "qwen3_vl_lora_sft.yaml"
MERGE_YAML_NAME = "qwen3_vl_lora_sft.yaml"

MEM0_ROOT = Path(__file__).resolve().parent.parent
LLAMAFACTORY_DATA_PREP_SCRIPT = MEM0_ROOT / "scripts" / "llama_data_preparation" / "llamafactory_data_preparation.py"


def get_dataset_name(lerobot_dataset_path: str) -> str:
    """Derive dataset name from LeRobot path (same as data prep script)."""
    return Path(lerobot_dataset_path).name or "dataset"


def get_json_filename(dataset_name: str) -> str:
    """JSON filename produced by data prep script."""
    return f"{dataset_name}_high_level_finetune_data.json"


def get_images_folder_name(dataset_name: str) -> str:
    """Images folder name produced by data prep script."""
    return f"{dataset_name}_images"


def run_cmd(cmd: list[str], cwd: Path | None = None, env_name: str | None = None) -> None:
    env = None
    if env_name:
        # --no-capture-output: 不捕获子进程输出，让 train 等步骤的日志实时打印到终端
        cmd = ["conda", "run", "--no-capture-output", "-n", env_name] + cmd
        # 子进程继承 PYTHONUNBUFFERED，避免 Python 缓冲导致日志延迟
        env = {**os.environ, "PYTHONUNBUFFERED": "1"}
    try:
        subprocess.run(cmd, cwd=cwd, check=True, env=env)
    except subprocess.CalledProcessError as e:
        print(f"Command failed: {cmd}", file=sys.stderr)
        sys.exit(e.returncode)


def step_prepare(cfg: dict) -> str:
    """Step 1: run data preparation in mem0 env; returns dataset_name."""
    lerobot_path = cfg["lerobot_dataset_path"]
    if not lerobot_path or not Path(lerobot_path).exists():
        raise SystemExit("lerobot_dataset_path is not set or path does not exist. Set it in run_planning_pipeline.sh.")
    dataset_name = get_dataset_name(lerobot_path)
    out_dir = MEM0_ROOT / "llamafactory_data" / dataset_name
    out_dir.mkdir(parents=True, exist_ok=True)

    cmd = [
        sys.executable,
        str(LLAMAFACTORY_DATA_PREP_SCRIPT),
        "--lerobot_dataset_path", str(Path(lerobot_path).resolve()),
        "--episode_start_id", str(cfg["episode_start_id"]),
        "--episode_end_id", str(cfg["episode_end_id"]),
    ]
    print("[Step 1] Running data preparation (mem0 env)...")
    run_cmd(cmd, cwd=MEM0_ROOT, env_name=cfg.get("conda_env_mem0"))
    print(f"[Step 1] Done. Output: {out_dir}")
    return dataset_name


def step_copy(cfg: dict, dataset_name: str) -> None:
    """Step 2: copy llamafactory_data/{dataset_name} to LlamaFactory/data and update dataset_info.json."""
    lf_root = Path(cfg["llamafactory_root"])
    if not lf_root.is_dir():
        raise SystemExit("llamafactory_root is not set or path does not exist. Set it in run_planning_pipeline.sh.")
    data_dir = lf_root / "data"
    data_dir.mkdir(parents=True, exist_ok=True)

    src = MEM0_ROOT / "llamafactory_data" / dataset_name
    if not src.is_dir():
        raise SystemExit(f"Data directory does not exist: {src}. Run step 'prepare' first.")

    json_name = get_json_filename(dataset_name)
    images_name = get_images_folder_name(dataset_name)
    src_json = src / json_name
    src_images = src / images_name
    if not src_json.is_file():
        raise SystemExit(f"File not found: {json_name}. Run step 'prepare' first.")

    shutil.copy2(src_json, data_dir / json_name)
    dst_images = data_dir / images_name
    if dst_images.exists():
        shutil.rmtree(dst_images)
    shutil.copytree(src_images, dst_images)
    print(f"[Step 2] Copied {json_name} and {images_name} to {data_dir}")

    # 更新 dataset_info.json
    info_path = data_dir / "dataset_info.json"
    if info_path.exists():
        with open(info_path, "r", encoding="utf-8") as f:
            info = json.load(f)
    else:
        info = {}

    info[dataset_name] = {
        "file_name": json_name,
        "formatting": "sharegpt",
        "columns": {"messages": "messages", "images": "images"},
        "tags": {
            "role_tag": "role",
            "content_tag": "content",
            "user_tag": "user",
            "assistant_tag": "assistant",
            "system_tag": "system",
        },
    }
    with open(info_path, "w", encoding="utf-8") as f:
        json.dump(info, f, indent=2, ensure_ascii=False)
    print(f"[Step 2] Updated {info_path} with dataset '{dataset_name}'.")


def step_train(cfg: dict, dataset_name: str) -> str:
    """Step 3: generate train YAML and run training; returns output_dir (adapter path)."""
    base_out = cfg["base_output_dir"]
    if not base_out:
        raise SystemExit("base_output_dir is not set. Set it in run_planning_pipeline.sh.")
    lf_root = Path(cfg["llamafactory_root"])
    # Use absolute path so LLaMA-Factory (running with cwd=LlamaFactory) loads local checkpoint, not HuggingFace
    base_model_path = Path(base_out).resolve() / BASE_MODEL_DIR_NAME
    if not base_model_path.is_dir():
        raise SystemExit(
            f"Base model dir not found: {base_model_path}\n"
            "Download Qwen3-VL-8B-Instruct and place it there, or set base_output_dir in run_planning_pipeline.sh."
        )
    output_dir = Path(base_out).resolve() / f"{dataset_name}_sft_lora"
    output_dir.mkdir(parents=True, exist_ok=True)

    train_config = {
        "model_name_or_path": str(base_model_path),
        "image_max_pixels": 262144,
        "video_max_pixels": 16384,
        "trust_remote_code": True,
        "stage": "sft",
        "do_train": True,
        "finetuning_type": "lora",
        "lora_rank": 8,
        "lora_target": "all",
        "dataset": dataset_name,
        "template": TEMPLATE,
        "cutoff_len": 2048,
        "max_samples": cfg.get("max_samples", 1000),
        "overwrite_cache": True,
        "preprocessing_num_workers": 16,
        "dataloader_num_workers": 8,
        "output_dir": str(output_dir),
        "logging_steps": 10,
        "save_steps": 500,
        "plot_loss": True,
        "overwrite_output_dir": True,
        "save_only_model": False,
        "report_to": cfg.get("report_to", "wandb"),
        "per_device_train_batch_size": cfg.get("per_device_train_batch_size", 16),
        "gradient_accumulation_steps": 1,
        "learning_rate": cfg.get("learning_rate", 1.0e-4),
        "num_train_epochs": cfg.get("num_train_epochs", 25),
        "lr_scheduler_type": "cosine",
        "warmup_ratio": 0.1,
        "bf16": True,
        "ddp_timeout": 180000000,
        "resume_from_checkpoint": None,
    }

    yaml_dir = lf_root / "examples" / "train_lora"
    yaml_dir.mkdir(parents=True, exist_ok=True)
    yaml_path = yaml_dir / TRAIN_YAML_NAME
    with open(yaml_path, "w", encoding="utf-8") as f:
        for k, v in train_config.items():
            if v is None:
                f.write(f"{k}: null\n")
            elif isinstance(v, bool):
                f.write(f"{k}: {str(v).lower()}\n")
            else:
                f.write(f"{k}: {v}\n")
    print(f"[Step 3] Wrote {yaml_path}")

    print("[Step 3] Running LLaMA-Factory train (llama_factory env)...")
    run_cmd(
        ["llamafactory-cli", "train", f"examples/train_lora/{TRAIN_YAML_NAME}"],
        cwd=lf_root,
        env_name=cfg.get("conda_env_llamafactory"),
    )
    print(f"[Step 3] Done. Adapter: {output_dir}")
    return str(output_dir)


def step_merge(cfg: dict, adapter_path: str, dataset_name: str) -> None:
    """Step 4: generate merge YAML and run LoRA merge."""
    export_dir = cfg.get("export_dir") or os.path.join(
        cfg["base_output_dir"], f"Qwen3-VL-8B-Instruct-{dataset_name}"
    )
    export_dir = Path(export_dir).resolve()
    lf_root = Path(cfg["llamafactory_root"])
    base_model_path = Path(cfg["base_output_dir"]).resolve() / BASE_MODEL_DIR_NAME

    merge_config = [
        "# DO NOT use quantized model or quantization_bit when merging lora adapters",
        "model_name_or_path: " + str(base_model_path),
        "adapter_name_or_path: " + adapter_path,
        "template: " + TEMPLATE,
        "trust_remote_code: true",
        "export_dir: " + str(export_dir),
        "export_size: " + str(cfg.get("export_size", 5)),
        "export_device: " + cfg.get("export_device", "cpu"),
        "export_legacy_format: false",
    ]

    yaml_dir = lf_root / "examples" / "merge_lora"
    yaml_dir.mkdir(parents=True, exist_ok=True)
    yaml_path = yaml_dir / MERGE_YAML_NAME
    with open(yaml_path, "w", encoding="utf-8") as f:
        f.write("\n".join(merge_config) + "\n")
    print(f"[Step 4] Wrote {yaml_path}")

    print("[Step 4] Running LLaMA-Factory export/merge (llama_factory env)...")
    run_cmd(
        ["llamafactory-cli", "export", f"examples/merge_lora/{MERGE_YAML_NAME}"],
        cwd=lf_root,
        env_name=cfg.get("conda_env_llamafactory"),
    )
    print(f"[Step 4] Done. Merged model: {export_dir}")


def main() -> None:
    parser = argparse.ArgumentParser(description="Planning Module: copy -> train -> merge (invoked by run_planning_pipeline.sh)")
    parser.add_argument(
        "--steps",
        nargs="+",
        default=["prepare", "copy", "train", "merge"],
        choices=["prepare", "copy", "train", "merge"],
        help="Steps to run",
    )
    parser.add_argument(
        "--no-conda-run",
        action="store_true",
        help="Do not use conda run; assume env is already activated",
    )
    parser.add_argument("--lerobot_dataset_path", type=str, default=None)
    parser.add_argument("--llamafactory_root", type=str, default=None)
    parser.add_argument("--base_output_dir", type=str, default=None)
    parser.add_argument("--export_dir", type=str, default=None)
    parser.add_argument("--episode_start_id", type=int, default=None)
    parser.add_argument("--episode_end_id", type=int, default=None)
    parser.add_argument("--max_samples", type=int, default=None)
    parser.add_argument("--num_train_epochs", type=int, default=None)
    parser.add_argument("--per_device_train_batch_size", type=int, default=None)
    parser.add_argument("--learning_rate", type=str, default=None)
    parser.add_argument("--report_to", type=str, default=None)
    parser.add_argument("--export_size", type=int, default=None)
    parser.add_argument("--export_device", type=str, default=None)
    parser.add_argument("--conda_env_mem0", type=str, default=None)
    parser.add_argument("--conda_env_llamafactory", type=str, default=None)
    args = parser.parse_args()

    cfg = dict(USER_CONFIG)
    for key, val in [
        ("lerobot_dataset_path", args.lerobot_dataset_path),
        ("llamafactory_root", args.llamafactory_root),
        ("base_output_dir", args.base_output_dir),
        ("export_dir", args.export_dir),
        ("episode_start_id", args.episode_start_id),
        ("episode_end_id", args.episode_end_id),
        ("max_samples", args.max_samples),
        ("num_train_epochs", args.num_train_epochs),
        ("per_device_train_batch_size", args.per_device_train_batch_size),
        ("learning_rate", args.learning_rate),
        ("report_to", args.report_to),
        ("export_size", args.export_size),
        ("export_device", args.export_device),
        ("conda_env_mem0", args.conda_env_mem0),
        ("conda_env_llamafactory", args.conda_env_llamafactory),
    ]:
        if val is not None:
            cfg[key] = val
    if args.no_conda_run:
        cfg["conda_env_mem0"] = None
        cfg["conda_env_llamafactory"] = None

    steps = args.steps
    dataset_name = None
    adapter_path = None

    if "prepare" in steps:
        dataset_name = step_prepare(cfg)
    if "copy" in steps:
        if dataset_name is None:
            dataset_name = get_dataset_name(cfg["lerobot_dataset_path"])
            if not cfg["lerobot_dataset_path"]:
                raise SystemExit("Set lerobot_dataset_path in run_planning_pipeline.sh or run step 'prepare' first.")
        step_copy(cfg, dataset_name)
    if "train" in steps:
        if dataset_name is None:
            dataset_name = get_dataset_name(cfg["lerobot_dataset_path"])
            if not cfg["lerobot_dataset_path"]:
                raise SystemExit("Set lerobot_dataset_path in run_planning_pipeline.sh or run step 'prepare' first.")
        adapter_path = step_train(cfg, dataset_name)
    if "merge" in steps:
        if dataset_name is None:
            dataset_name = get_dataset_name(cfg["lerobot_dataset_path"])
        if not adapter_path:
            base_out = cfg["base_output_dir"]
            if not base_out:
                raise SystemExit("base_output_dir is not set. Set it in run_planning_pipeline.sh.")
            adapter_path = str(Path(base_out).resolve() / f"{dataset_name}_sft_lora")
        step_merge(cfg, adapter_path, dataset_name)

    print("All requested steps completed.")


if __name__ == "__main__":
    main()
